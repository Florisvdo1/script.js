// Save and Load State
function saveState() {
  const state = {};
  placeholders.forEach((ph, index) => {
    state[index] = ph.textContent;
  });
  localStorage.setItem('emojiState', JSON.stringify(state));
}

function loadState() {
  const state = JSON.parse(localStorage.getItem('emojiState'));
  if (state) {
    placeholders.forEach((ph, index) => {
      if (state[index]) {
        ph.textContent = state[index];
      }
    });
  }
}

